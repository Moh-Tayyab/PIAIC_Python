#List
# A list is a collection which is ordered and changeable. Allows duplicate members.
# Lists are written with square brackets.
# Example:
#index      0 1 2 3 4
num_list = [1,2,3,4,5]
print(num_list)

#indexing
print(num_list[0]) #1
print(num_list[1]) #2
print(num_list[2]) #3
print(num_list[3]) #4
print(num_list[4]) #5
