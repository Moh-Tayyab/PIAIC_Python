# Q2: Arithmetic Operators
# Let x = 20 and y = 6.
# Perform and print the results of:
# ● Addition
# ● Subtraction
# ● Multiplication
# ● Division
# ● Floor Division
# ● Modulus
# ● Exponentiation

# Solution:

x: int = 20
y: int = 6

# Addition
addition: int = x + y
print("Addition:", addition)

# Subtraction
subtraction: int = x - y
print("Subtraction:", subtraction)

# Multiplication
multiplication: int = x * y	
print("Multiplication:", multiplication)

# Division
division: float = x / y
print("Division:", division)

# Floor Division
floor_division: int = x // y
print("Floor Division:", floor_division)

# Modulus
modulus: int = x % y
print("Modulus:", modulus)

# Exponentiation
exponentiation: int = x ** y
print("Exponentiation:", exponentiation)

